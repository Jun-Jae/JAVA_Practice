package org.opentutorials.javatutorials.method;

public class MethodDemo2 {

	public static void main(String[] args) {
		int i = 0;
		while(i < 10) {
			System.out.println(i);
			i++;
		}
		
		i = 0;
		while(i < 10) {
			System.out.println(i);
			i++;
		}

	}

} // 메소드를 사용하면 이미 정의해둔 로직을 재활용할 수 있다.
  // 메소드를 사용 코드의 양을 줄여 유지보수에 용이하다.
